function [ Force ] = GetMaxBrakeForce( v )
%计算列车最大电制动力（外包络）
%输入参数：
    %v 列车速度，单位：km/h
%输出参数：
    %Force 列车再生制动力 单位：KN
Force=(v>=0 & v<=15).*(360)+(15<v & v<=16).*(172.*v-2220)+(16<v &v<=70).*(532)+(70<v & v<=120).*(-1.68.*v+649.6)+(120<v & v<=350).*(-0.6522.*v+526.3);
% Force=(v>=0 & v<=10).*(0)+(10<v & v<=72).*(220)+(72<v &v<=290).*(-0.2064.*v+234.9)+(290<v & v<=350).*(-0.6667.*v+368.3);
end